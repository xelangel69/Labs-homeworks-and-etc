package Characters;

/**
 * Перечисление возможных эмоциональных состояний (настроений) персонажей.
 */
public enum Mood {

    /** Стандартное, спокойное состояние. */
    DEFAULT("спокоен"),

    /** Состояние веселья. */
    JOYFUL("озорной"),

    /** Состояние счастья. */
    HAPPY("счастлив"),

    /** Состояние раздражения. */
    ANNOYED("раздражён"),

    /** Состояние злости. */
    ANGRY("зол"),

    /** Состояние сильной ярости. */
    FURIOUS("в бешестве"),

    /** Состояние страха. */
    SCARED("испуган");

    /** Текстовое описание настроения. */
    private final String description;

    /**
     * Конструктор элемента перечисления.
     *
     * @param description строковое описание настроения (например, "спокоен" или "зол").
     */
    Mood(String description){
        this.description = description;
    }

    /**
     * Возвращает текстовое описание настроения.
     *
     * @return строка с описанием текущего состояния.
     */
    public String getDescription() {
        return description;
    }
}